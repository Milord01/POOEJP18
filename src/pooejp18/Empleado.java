    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int codigo;
        String nombre;
	double horas,valorhora,retencionP,salariobruto, salarioneto,retencion;
        
	horas=Double.parseDouble(txtHoras.getText());
        valorhora=Double.parseDouble(txtValorHora.getText());
        retencionP=Double.parseDouble(txtRetencionP.getText());
        
        salariobruto=horas*valorhora;
	retencion=salariobruto*(retencionP/100);
	salarioneto=salariobruto-retencion;
        
        txtNeto.setText(String.valueOf(salarioneto));
        txtBruto.setText(String.valueOf(salariobruto));
        

    }//GEN-LAST:event_btnCalcularActionPerformed

